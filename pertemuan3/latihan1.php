<?php
// Pengulangan
// for, while, do.. while, foreach (Pengulangan khusus array)
// For
for($i = 0; $i < 5; $i++){
    echo "Hello World! <br>";
}

// While
$i = 0;
while($i < 5){
    echo "Hello World2! <br>";
$i++;
}
// do.. while
$i = 0;
do{
    echo "Hello World3! <br>";
$i++;
} while($i < 5);





?>